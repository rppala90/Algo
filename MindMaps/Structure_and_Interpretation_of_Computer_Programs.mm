    <p>
      <b>Mathematical Functions Vs Computer Procedures</b>:
    </p>
    <p>
      The contrast between function and procedure is a reflection of the general distinction between describing properties of things and describing how to do things.
    </p>
    <p>
      Functions are like declarative descriptions (What is ?)
    </p>
    <p>
      Procedures are like Imperative descriptions (How to ?)
    </p>
    <p>
      Think of &quot;what is sqrt&quot; Vs &quot;How to compute sqrt&quot;
    </p>
    <p>
      
    </p>
    <p>
      <b>Linear Recursion and Iteration.</b>
    </p>
    <p>
      In the iterative case, the program variables provide a complete description of the state of the process at any point. In the recursive case, there is some additional 'hidden' information, maintained by the interpreter and not contained in the program variables, which indicates &quot;where the program is&quot; in negotiating the chain of deferred operations. Longer the chain, more information has to be maintained.
    </p>
